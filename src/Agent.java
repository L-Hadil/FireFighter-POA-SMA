public abstract class Agent {
    protected int x;
    protected int y;

    public Agent(int startX, int startY) {
        this.x = startX;
        this.y = startY;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }


    public void setX(int newX) {
        this.x = newX;
    }

    public void setY(int newY) {
        this.y = newY;
    }

    public abstract void move(); // Méthode abstraite à implémenter dans les sous-classes
}
